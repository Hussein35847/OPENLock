function positionSuggestionBox(input) {
    if (!suggestionBox) return;

    const rect = input.getBoundingClientRect();
    const boxHeight = 160;
    const boxWidth = 300;

    let top = rect.bottom + window.scrollY + 8;
    let left = rect.left + window.scrollX;

    if (top + boxHeight > window.innerHeight + window.scrollY) {
        top = rect.top + window.scrollY - boxHeight - 8;
    }

    if (left + boxWidth > window.innerWidth) {
        left = window.innerWidth - boxWidth - 10;
    }

    if (left < 0) {
        left = 10;
    }

    suggestionBox.style.top = top + 'px';
    suggestionBox.style.left = left + 'px';
}

function showSuggestion(input) {
    createSuggestionBox();
    activeInput = input;
    positionSuggestionBox(input);
    suggestionBox.style.display = 'block';
}

function hideSuggestion() {
    if (suggestionBox) {
        suggestionBox.style.display = 'none';
    }
}

// Listen for password field focus
document.addEventListener('focus', (e) => {
    if (e.target.type === 'password') {
        showSuggestion(e.target);
        
        if (isSignupPage() && !e.target.value && !autoGeneratedPassword) {
            setTimeout(() => {
                const generateBtn = document.getElementById('op-generate-btn');
                if (generateBtn) {
                    generateBtn.click();
                }
            }, 300);
        }
    }
}, true);

// Listen for focus loss
document.addEventListener('blur', (e) => {
    if (e.target.type === 'password') {
        setTimeout(hideSuggestion, 200);
    }
}, true);

// Handle clicks outside suggestion box
document.addEventListener('click', (e) => {
    if (suggestionBox && e.target !== suggestionBox && !suggestionBox?.contains(e.target)) {
        hideSuggestion();
    }
}, true);

// Handle form submission - offer to save
document.addEventListener('submit', (e) => {
    const form = e.target;
    const passwordInputs = form.querySelectorAll('input[type="password"]');
    
    if (passwordInputs.length > 0) {
        const password = passwordInputs[0].value;
        const usernameField = findUsernameField();
        const username = usernameField?.value || '';
        
        if (password && password.length > 6) {
            if (isSignupPage()) {
                e.preventDefault();
                showSavePrompt(password, username, form);
            } else {
                chrome.runtime.sendMessage({
                    type: 'formSubmitted',
                    website: window.location.hostname,
                    password: password,
                    username: username
                }).catch(() => {});
            }
        }
    }
}, true);

