// Detects password fields, autofills credentials, and prompts to save

function generateStrongPassword() {
    const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789!@#$%^&*()_+~|}{[]:;?><,./-=';
    const array = new Uint32Array(20);
    window.crypto.getRandomValues(array);
    let pwd = '';
    for (let i = 0; i < array.length; i++) {
        pwd += chars[array[i] % chars.length];
    }
    return pwd;
}

// Detect if current page is a signup/register form
function isSignupPage() {
    const url = window.location.href.toLowerCase();
    const html = document.documentElement.innerHTML.toLowerCase();
    
    const signupPatterns = [/signup|register|join|create.*account|new.*account|sign.*up|enroll|join.*now/];
    const isSignupUrl = signupPatterns.some(p => p.test(url));
    
    const hasConfirmPassword = Array.from(document.querySelectorAll('input[type="password"]')).length > 1 ||
                               html.includes('confirm password') || 
                               html.includes('repeat password') ||
                               html.includes('password again');
    
    const signupText = [/sign up|register|create account|join now|get started|sign up/];
    const hasSignupText = signupText.some(p => p.test(html));
    
    return isSignupUrl || (hasConfirmPassword && hasSignupText);
}

// Find username or email input fields
function findUsernameField() {
    const inputs = document.querySelectorAll('input[type="email"], input[type="text"], input[name*="user"], input[name*="email"], input[name*="login"]');
    for (let input of inputs) {
        const name = (input.name || input.id || '').toLowerCase();
        const placeholder = (input.placeholder || '').toLowerCase();
        if (name.includes('email') || name.includes('user') || name.includes('login') || 
            placeholder.includes('email') || placeholder.includes('user') || placeholder.includes('username')) {
            return input;
        }
    }
    return inputs[0];
}

